# Values for demonstration
a = 10
b = 3

print("=== Arithmetic Operators ===")
print("a + b =", a + b)
print("a - b =", a - b)
print("a * b =", a * b)
print("a / b =", a / b)
print("a % b =", a % b)
print("a ** b =", a ** b)
print("a // b =", a // b)

print("\n=== Comparison Operators ===")
print("a == b:", a == b)
print("a != b:", a != b)
print("a > b:", a > b)
print("a < b:", a < b)
print("a >= b:", a >= b)
print("a <= b:", a <= b)

print("\n=== Logical Operators ===")
x = True
y = False
print("x and y:", x and y)
print("x or y:", x or y)
print("not x:", not x)
